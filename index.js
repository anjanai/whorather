var container = document.getElementById("celebCont");
container.style.opacity="100%";
const urlParams = new URLSearchParams(window.location.search);
n = urlParams.get('n');
if (!n) n=0;
var celebs = celeb_bank[n];

// CELEB IMGS--------------------------------------------------------------
var i = celebs.length;
j= 0;

function create_image (num) {
    let img = document.createElement("img");
    img.src = celebs[j++];
    img.className="img" + num;
    let box, xbox;
    
    if (num == 1) {
	box = document.getElementById("celebbox1");
	xbox = document.getElementById("celebbox2");
    } else {
	box = document.getElementById("celebbox2");
	xbox = document.getElementById("celebbox1");
    }
    box.appendChild(img);
    $(box).click(function(){
	if (j == celebs.length) {
	    xbox.style.display="none";
	    var main = document.getElementsByTagName('main')[0];
	    main.style.backgroundImage="url(https://bestanimations.com/media/many-hearts/1391146284gold-falling-hearts-animated-gif.gif)"; 
	}
	else xbox.children[0].src = celebs[j++];
    })
}

create_image(1);
